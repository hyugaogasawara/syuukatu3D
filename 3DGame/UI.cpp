//=============================================================================
// 静的メンバ変数宣言
//=============================================================================

//=============================================================================
// デフォルトコンストラクタ
//=============================================================================
CUI::CUI(int nPriority) : CScene2D(nPriority)
{
	// メンバ変数のクリア
	m_pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);	// 位置
	m_size = D3DXVECTOR3(0.0f, 0.0f, 0.0f);	// 大きさ
	m_move = D3DXVECTOR3(0.0f, 0.0f, 0.0f);	// 移動量
	m_nType = 0;		// テクスチャの種類
}
//=============================================================================
// デストラクタ
//=============================================================================
CUI::~CUI()
{
}
//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CUI::Init(void)
{
	// オブジェクトの種類設定
	SetObjType(OBJTYPE_BULLET);

	// 継承元の初期化処理
	CScene2D::Init();
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CUI::Uninit(void)
{
	// 継承元の終了処理
	CScene2D::Uninit();

	// オブジェクトを開放
	Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CUI::Update(void)
{
	m_pos = CScene2D::GetPosition();
	m_size = CScene2D::GetSize();


	// 位置と大きさを設定
	CScene2D::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CUI::Draw(void)
{
	// 継承元の描画処理
	CScene2D::Draw();

}
//=============================================================================
// 生成処理
//=============================================================================
CUI *CUI::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, D3DXVECTOR3 move, int nType)
{
	CUI *pUI = nullptr;
	if (pUI == nullptr)
	{
		pUI = new CUI;

		if (pUI != nullptr)
		{
			pUI->m_pos = pos;		// 位置
			pUI->m_size = size;		// 大きさ
			pUI->m_move = move;		// 移動量
			pUI->BindTexture(nType);// テクスチャの種類
			pUI->Init();			// 初期化処理

		}
	}
	return pUI;
}
