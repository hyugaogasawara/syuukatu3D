#include "select.h"
#include "keyboard.h"
#include "fade.h"
#include "UI.h"
//=============================================================================
// 静的メンバ変数宣言
//=============================================================================
CUI *CSelect::m_pUI = nullptr;

//=============================================================================
// デフォルトコンストラクタ
//=============================================================================
CSelect::CSelect(int nPriority) : CScene(nPriority)
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CSelect::~CSelect()
{
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CSelect::Init(void)
{
	// UI
	m_pUI = CUI::Create(D3DXVECTOR3(SCREEN_CENTER_X, SCREEN_CENTER_Y, 0.0f), D3DXVECTOR3(300.0f, 300.0f, 0.0f), D3DXVECTOR3(0.0f, 0.0f, 0.0f), 7);



	return S_OK;
}
//=============================================================================
// 終了処理
//=============================================================================
void CSelect::Uninit(void)
{
	// オブジェクトの開放
	Release();
}
//=============================================================================
// 更新処理
//=============================================================================
void CSelect::Update(void)
{
	// フェードの取得
	CFade *pFade = CManager::GetFade();

	if (pFade->GetFade() == CFade::FADE_NONE)
	{
		if (CManager::GetInputKeyboard()->GetTrigger(DIK_SPACE) == true)
		{
			pFade->SetFade(CManager::MODE_GAME);
		}
	}

}
//=============================================================================
// 描画処理
//=============================================================================
void CSelect::Draw(void)
{
}
//=============================================================================
// 生成処理
//=============================================================================
CSelect *CSelect::Create(void)
{
	CSelect *pSelect = nullptr;

	if (pSelect == nullptr)
	{
		pSelect = new CSelect;

		if (pSelect != nullptr)
		{
			pSelect->Init();
		}
	}
	return pSelect;
}